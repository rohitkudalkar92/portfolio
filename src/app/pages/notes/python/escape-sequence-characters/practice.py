# Escape Sequence Characters Practice

# 1. Newline character \n
print("Hello\nWorld")
print("Line 1\nLine 2\nLine 3")

# 2. Tab character \t
print("Name:\tJohn")
print("Age:\t25")
print("City:\tNew York")

# 3. Backslash character \\
print("File path: C:\\Users\\Documents")
print("This is a backslash: \\")